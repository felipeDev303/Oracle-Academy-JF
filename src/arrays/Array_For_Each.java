package arrays;

public class Array_For_Each {
    public static void main (String[] args){

        // Declaración e inicialización estática del array
        String [] paises={"España","Chile","Argentina","Colombia","México","Perú","Ecuador","Venezuela"};

        // For-each
        for(String i:paises){
            System.out.println(i);
        }
    }
}
